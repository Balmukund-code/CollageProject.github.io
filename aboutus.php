Welcome to Mau Polytechnic College
Mau Polytechnic College came into existence in 1956 due to the missionary zeal of the visionary Late Lala Karam Chand Thapar. It is one of the three organizations on Thapar Technology Campus, the other two are Thapar Institute of Engineering and Technology University and Thapar Centre for Industrial Research & Development. It has been registered as a Society under Registration of Societies Act 1860. TPC is a privately managed, Government aided institution affiliated to Punjab State Board of Technical Education and Industrial Training, Chandigarh. During the past over five decades, TPC has established itself into a leading Polytechnic offering 3-year Diploma in 05 disciplines i.e. Civil Engg., Electrical Engg. Mechanical Engg, Computer Engg, Architectural Assistantship with a total intake of 360 students. Thapar Polytechnic College adjudged the Best Polytechnic in Northern Region in the year 1999-2000 and 2005-06.
Vision
To be recognized as a leader committed to excellence in technical education, innovation and skill development that meets the aspirations of the society.
Mission
To advance the cause of Technical Education through creation and dissemination of knowledge and innovations in teaching and learning process.
To produce globally competent engineers having entrepreneurial skills and ethical values.
To contribute in rural development, community services and improvement in employability through conduct of skill development courses and training.
To foster sense of partnership with industry for meeting contemporary technical challenges of the Society.

